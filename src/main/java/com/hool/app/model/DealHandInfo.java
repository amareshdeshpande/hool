package com.hool.app.model;

import java.util.List;

public class DealHandInfo {
	private GameTable table;
	private List<DealHand> dealHands;
	
	public DealHandInfo(GameTable table, List<DealHand> dealHands){	
		this.table=table;
		this.dealHands=dealHands;
	}
	
	public DealHandInfo(){	
	}
	
	public GameTable getTable() {
		return table;
	}
	public void setTable(GameTable table) {
		this.table = table;
	}
	public List<DealHand> getDealHands() {
		return dealHands;
	}
	public void setDealHands(List<DealHand> dealHands) {
		this.dealHands = dealHands;
	}
  
}
