package com.hool.app.model;

import java.util.List;

public class DealHand {
	private int directionCode;
	private String directionName;
	private List<String> cards;
	private HandStats handStats;
	
	public DealHand(){
		
	}
	
	public DealHand(int directionCode, String directionName, List<String> cards,HandStats handStats){
		this.directionCode = directionCode;
		this.directionName= directionName;
		this.cards = cards;
		this.handStats=handStats;		
	}
	
	public int getDirectionCode() {
		return directionCode;
	}
	public void setDirectionCode(int directionCode) {
		this.directionCode = directionCode;
	}
	
	public String getDirectionName() {
		return directionName;
	}
	public void setDirectionName(String directionName) {
		this.directionName = directionName;
	}
	public List<String> getCards() {
		return cards;
	}
	public void setCards(List<String>cards) {
		this.cards = cards;
	}
}
